
x = 3.14
y = 4
z = 5

result = round(x) #a função round() é usada para arredondar um número
result = abs(y) # a função abs() é usada para encontrar o valor absoluto(ou seja, a distância de um número até o zero)
result = pow(4, 3) # a função pow() é usada para calcular a potência de um número  {pow(base, exp)} (função de potência)
result = max(x, y, z) #a função max() é usada para descobrir o valor máximo entre variáveis
result = min(x, y, z) # a função min() é usada para descobrir o valor mínimo entre variáveis

print(result)